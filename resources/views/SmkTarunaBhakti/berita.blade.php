      <div class="p-artikel">
        <p>
          Pada hari Sabtu, 3 Desember 2022 SMK Taruna Bhakti Depok melaksanakan kegiatan rutin tahunan, yakni pertemuan pihak sekolah dengan orang tua/wali siswa kelas XI dan XII tahun pelajaran 2022/2023. Pertemuan tersebut dilaksanakan di Padepokan Setia Hati Terate – Cilodong yang di bagi menjadi 2 sesi. Sesi 1, pertemuan dengan orang tua/wali siswa kelas XII yang berlangsung dari pukul 07.30 – 09.50 WIB. Sesi 2, pertemuan dengan orang tua/wali siswa kelas XI yang berlangsung dari pukul 10.00 – 12.30 WIB.
        </p>

        <br>
        
        <p>
          Pertemuan ini juga turut di hadiri Bpk. Drs. Warjon (Wakil Ketua Yayasan Setya Bhakti), Bpk. Nursidik, ST (Kepala SMK Taruna Bhakti), Seluruh Wakil Kepala Sekolah, Kepala Program Keahlian, BP/BK, seluruh Wali Kelas XII dan XI.
        </p>
