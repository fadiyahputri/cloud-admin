        <p>
          Peranan orang tua/wali siswa sebagai rekan guru dalam mendidik siswa tidak dapat disepelekan. Materi sosialisasi program sekolah meliputi : Kelas XII
        </p>
        <ul>
          <li>Bidang Kurikulum, seperti : Kegiatan Akhir Tahun (PAS Semester Ganjil, Uji Level, Uji Kompetensi Keahlian, Ujian Sekolah), Pelepasan Siswa Kelas XII serta Sertifikasi Internasional</li>
          <li>Bidang Kesiswaan, seperti : Tata Tertib Siswa, sanksi pelanggaran, program beasiswa dan buku tahunan</li>
          <li>Bidang Hubungan Industri, seperti : Program Magang/PKL, Pembuatan Laporan dan Sidang PKL, Budaya Kerja perusahaan, Starbhak Day, dan Job Fair.</li>
          <li>Perkenalan wali kelas, guru BP/BK dan Kepala Program Keahlian.</li>
       </ul>
      </div>

      <br>      

      <div class="p-artikel-ul">
        <p>Kelas XI</p>
        <ul>
          <li>Bidang Kurikulum, seperti : Kegiatan Uji Level seluruh program keahlian</li>
          <li>Bidang Kesiswaan, seperti : Tata Tertib Siswa, sanksi pelanggaran, Kegiatan Ekstrakurikuler</li>
          <li>Bidang Hubungan Industri, seperti : Kegiatan Kunjungan Industri, Guru Tamu dari DUDIKA, Kelas Industri</li>
          <li>Perkenalan wali kelas, guru BP/BK dan Kepala Program Keahlian.</li>
       </ul>
