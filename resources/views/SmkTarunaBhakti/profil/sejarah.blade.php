      <p>
        Berawal dari tingginya minat siswa lulusan siswa SMP yang berdomisili di lingkungan Kecamatan Cimanggis Depok untuk melanjutkan pendidikan ke jenjang SMA/SMK, serta masih minimnya fasilitas SMK di Kecamatan Cimanggis.
        Yayasan Setya Bhakti dalam hal ini yang telah terlebih dahulu memiliki telah SMP Taruna Bhakti Depok yang berdiri dari tahun 1987 serta memilii siswa sekitar 1200 siswa. Atas dasar tersebut yayasa setya Bhakti membuka sarana pendidikan yang lebih tinggi yaitu SMK Taruna Bhakti Depok, dengan harapan sebagian lulusan dari SMP taruna Bhakti dapat tertampung di dalam lingkungan SMK Taruna Bhakti Depok.
      </p>
      <br>
      <p>
        Maka dengan tekad yang sudah bulat maka pada tanggal 16 Juni 2004 didirikan SMK Taruna Bhakti Depok yang berada di Jalan Pekapuran Kelurahan Curug Kecamatan Cimanggis Depok. Dengan Bidang Studi Keahlian “ Teknologi informasi dan komunikasi” serta kompetensi keahlian “Teknik computer dan jaringan”.
      </p>
      <br>
      <p>
        Ketika pertama berdiri keadaan sekolah dengan kondisi tiga ruang belajar permanen tetapi belum dilengkapi dengan jendela dan pintu ruangan. Pada tahun pertama berdiri SMK Taruna Bhakti mempunyai 3 rombongan belajar dengan total 126 siswa. Dengan jumlah guru pengajar 13 orang. Pada tahun pertama semester ke 2 SMK Taruna Bhaki baru mempunyai 1 Ruangan Yang di fungsikan sebagai Laboratorium computer.
      </p>
      <br>
      <p>
        Pada tahun kedua SMK Taruna Bhakti mendapatkan siswa dengan jumlah 64 siswa, jauh menurun dari tahun pertama disebabkan tingginya biaya pedidikan di lingkunan SMK Taruna Bhakti Depok.
      </p>
      <br>
      <p>
        Pada tahun ketiga dengan perubahan sitem Managemen yang sebelumnya terpusat di yayasan menjadi Management Berbasis Sekolah, maka pada tahun tersebut SMK Taruna Bhakti mendapatkan siswa 4 rombingan belajar dengan total siswa 163 siswa.
      </p>
      <br>
      <p>
        Pada tahun keempat dengan penambahan ruang belajar serta sarana Praktek computer SMK Taruna Bhakti mendapatkan siswa 5 rombongan belajar dengan jumlah siswa 206 siswa
      </p>
      <br>
      <p>
        Pada tahun Kelima dengan sarana yang lengkap serta prestasi yang diraih oleh SMK Taruna Bhakti yaitu memperoleh nilai Akreditasi Sekolah dengan Nilai “A” serta memperoleh rekor MURI dalam bidang teknologi Informasi dan Komunikasi. Maka pada tahun tersebut SMK Taruna Bhakti mendapatkan siswa sebanyak 6 Rombongan Belajar dengan jumlah 242 siswa.
      </p>
      <br>
      <p>
        Pada Tahun keenam Dibuka Kompetensi Keahlian “Teknik Multimedia” yang masih satu rumpun dengan teknik Komputer dan jaringan, SMK Taruna Bhakti mendapatkan siswa 9 rombongan belajar dengan jumlah siswa 360  siswa. Pada tahun ketujuh SMK Taruna Bhakti mendapatkan 8 rombongan belajar dengan jumlah 339 siswa.
      </p>
      <br>
      <p>
        Hingga saat ini dari fasilitas sarana dan prasarana penunjang kegiatan belajar mengajar berupa lab Komputer, SMK Taruna Bhakti telah mempunya 11 Lab Komputer dengan jumlah 20 PC/lab. 
      </p>
